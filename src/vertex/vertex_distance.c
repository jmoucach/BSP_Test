#include "../../hdr/doom.h"

double vertex_distance(t_vertex p1, t_vertex p2)
{
	return (sqrt((p1.x - p2.x) * (p1.x - p2.x)
		+ (p1.y - p2.y) * (p1.y - p2.y)));
}